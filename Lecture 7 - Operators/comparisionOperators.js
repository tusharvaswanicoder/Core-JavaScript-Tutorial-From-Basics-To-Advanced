/*
Comparision operators are used to compare two operands and return a boolean value.

All of these are binary operators.

> -> greater than
< -> less than
>= -> greater than or equal to
<= -> less than or equal to
== -> equal to
=== -> equal to
!= -> not equal to
!== -> not equal to
*/

// > -> greater than

console.log(2 > 3);

// < -> less than

console.log(2 < 3);

// >= -> greater than or equal to

console.log(2 >= 3);
console.log(2 >= 2);

// <= -> less than or equal to

console.log(2 <= 3);
console.log(2 <= 2);

// == -> equal to

console.log(2 == 3);
console.log(3 == 3);
console.log(2 == "2");

// === -> strict equal to

console.log(2 === 3);
console.log(3 === 3);
console.log(2 === "2");

// != -> not equal to

console.log(2 != 3);
console.log(3 != 3);
console.log(2 != "2");

// !== -> strict not equal to

console.log(2 !== 3);
console.log(3 !== 3);
console.log(2 !== "2");
